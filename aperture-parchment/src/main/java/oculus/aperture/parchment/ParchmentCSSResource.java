import oculus.aperture.common.rest.ApertureServerResource;

import org.restlet.data.MediaType;
import org.restlet.representation.StringRepresentation;
import org.restlet.resource.Get;

import com.google.inject.Inject;
import com.google.inject.name.Named;

/**
 * @author djonker
 *
 */
public class ParchmentCSSResource extends ApertureServerResource {

	private final String css;
	
	/**
	 * Parchment image resource.
	 */
	@Inject
	public ParchmentCSSResource(@Named("aperture.parchment.css") String css) {
		this.css = css;
	}

	@Get
	public StringRepresentation geCSS() {
		StringRepresentation sr = new StringRepresentation(css);
		sr.setMediaType(MediaType.TEXT_CSS);
		
		return sr;
	}

}
